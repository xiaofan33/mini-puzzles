/**
 * 扫描 ui 目录下所有组件模块，生成 _all.ts 和 index.ts，
 * 通过 export * as Shadcn 实现 tree-shakeable 的命名空间导出。
 */
import { readdir, writeFile } from 'node:fs/promises'
import { join } from 'node:path'

const UI_DIR = join(import.meta.dirname, '..', 'src', 'components', 'ui')
const ALL_PATH = join(UI_DIR, '_all.ts')
const INDEX_PATH = join(UI_DIR, 'index.ts')

async function generateIndex() {
  const files = await readdir(UI_DIR)
  const modules = files
    .filter(f => f.endsWith('.tsx') || f.endsWith('.ts'))
    .filter(f => f !== 'index.ts' && f !== '_all.ts')
    .map(f => f.replace(/\.\w+$/, ''))
    .sort()

  const reExports = modules.map(m => `export * from './${m}'`).join('\n')

  const allContent = `// Auto-generated by scripts/gen-shadcn-index.mjs
// Pure re-export module — enables tree-shaking through the namespace chain.
${reExports}
`

  const indexContent = `// Auto-generated by scripts/gen-shadcn-index.mjs
export * as Shadcn from './_all'
`

  await writeFile(ALL_PATH, allContent, 'utf-8')
  await writeFile(INDEX_PATH, indexContent, 'utf-8')
  console.log(
    `Generated ${ALL_PATH} and ${INDEX_PATH} from ${modules.length} modules`,
  )
}

generateIndex().catch(console.error)
