/**
 * 扫描 ui 目录下所有组件的导出，生成 index.ts 中的 Shadcn 命名空间对象。
 */
import { readdir, readFile, writeFile } from 'node:fs/promises'
import { join } from 'node:path'

const UI_DIR = join(import.meta.dirname, '..', 'src', 'components', 'ui')
const INDEX_PATH = join(UI_DIR, 'index.ts')

async function scanExports() {
  const files = await readdir(UI_DIR)
  const tsxFiles = files
    .filter(f => f.endsWith('.tsx') || f.endsWith('.ts'))
    .filter(f => f !== 'index.ts')

  const allExports = []

  for (const file of tsxFiles) {
    const content = await readFile(join(UI_DIR, file), 'utf-8')
    const moduleName = file.replace(/\.\w+$/, '')
    const modulePath = `./${moduleName}`

    // 匹配 export { A, B, C } 形式
    const namedExportMatch = content.match(/export\s*\{([^}]+)\}/g)
    if (namedExportMatch) {
      for (const block of namedExportMatch) {
        const names = block
          .replace(/export\s*\{/, '')
          .replace(/\}/, '')
          .split(',')
          .map(s =>
            s
              .trim()
              .split(/\s+as\s+/)
              .pop()
              ?.trim(),
          )
          .filter(Boolean)
        for (const name of names) {
          allExports.push({ name, module: modulePath })
        }
      }
    }

    // 匹配 export function Xxx / export const Xxx / export class Xxx 形式
    const inlineExports = content.matchAll(
      /export\s+(?:function|const|class)\s+(\w+)/g,
    )
    for (const match of inlineExports) {
      allExports.push({
        name: match[1],
        module: modulePath,
      })
    }
  }

  return allExports
}

async function generateIndex() {
  const exports = await scanExports()

  // 按 module 分组
  const moduleMap = new Map()
  for (const { name, module } of exports) {
    if (!moduleMap.has(module)) moduleMap.set(module, [])
    moduleMap.get(module).push(name)
  }

  // 生成 import 语句
  const imports = []
  for (const [module, names] of moduleMap) {
    imports.push(`import { ${names.join(', ')} } from '${module}'`)
  }

  // 生成 Shadcn 对象
  const entries = exports.map(({ name }) => `  ${name},`).join('\n')

  const content = `// Auto-generated by scripts/gen-shadcn-index.mjs
${imports.join('\n')}

export const Shadcn = {
${entries}
} as const
`

  await writeFile(INDEX_PATH, content, 'utf-8')
  console.log(`Generated ${INDEX_PATH} with ${exports.length} exports`)
}

generateIndex().catch(console.error)
